import math
# Python has a set of built-in math functions, including an extensive math module,
# that allows you to perform mathematical tasks on numbers.

if __name__ == '__main__':
    # Built-in
    x = min(70, 45, 90, 5.5, 80, 43)
    y = max(70, 45, 90, 5.5, 80, 43)

    print("min = ", x)
    print("max = ", y)

    absolute_value = abs(-7.25)
    print("absolute = ", absolute_value)

    power = pow(3, 3)
    print("power = ", power)

